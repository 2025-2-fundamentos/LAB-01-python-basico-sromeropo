import pathlib

def pregunta_07():
    """
    Retorne una lista de tuplas que asocien las columnas 0 y 1. Cada tupla
    contiene un valor posible de la columna 2 y una lista con todas las letras
    asociadas (columna 1) a dicho valor de la columna 2.

    Rta/
    [(0, ['C']),
     (1, ['E', 'B', 'E']),
     (2, ['A', 'E']),
     (3, ['A', 'B', 'D', 'E', 'E', 'D']),
     (4, ['E', 'B']),
     (5, ['B', 'C', 'D', 'D', 'E', 'E', 'E']),
     (6, ['C', 'E', 'A', 'B']),
     (7, ['A', 'C', 'E', 'D']),
     (8, ['E', 'D', 'E', 'A', 'B']),
     (9, ['A', 'B', 'E', 'A', 'A', 'C'])]

    """
    resultado = {}
    current_path = pathlib.Path(__file__).parent
    data_file = current_path.parent / "files/input/data.csv"
    with open(data_file, "r") as file:
        for line in file:
            campos = line.strip().split("\t")
            letra = campos[0]
            numero = int(campos[1])
            if numero not in resultado:
                resultado[numero] = [letra]
            else:
                resultado[numero].append(letra)
    return sorted([(numero, letras) for numero, letras in resultado.items()])
